    private void studentadminsubmitbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentadminsubmitbuttonActionPerformed
        // TODO add your handling code here:
        try{
            String rollno = jTextField1.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Pankajjha2020@");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM student INNER JOIN result WHERE student.rollno = '"+rollno+"' AND result.Rollno = '"+rollno+"'");

            
            if(rs.next()){
                setVisible(false);
                new studentHome(rollno).setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null,"Incorrect Roll Number!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_studentadminsubmitbuttonActionPerformed

